package client.model;

public class ProdProvId {

    private String cod;
    private String cif;
    
    public ProdProvId(){}

    public ProdProvId(String cod, String cid) {
        this.cod = cod;
        this.cif = cid;
    }
    

    /**
     * @return the cod
     */
    public String getCod() {
        return cod;
    }

    /**
     * @param cod the cod to set
     */
    public void setCod(String cod) {
        this.cod = cod;
    }

    /**
     * @return the cif
     */
    public String getCif() {
        return cif;
    }

    /**
     * @param cif the cif to set
     */
    public void setCif(String cif) {
        this.cif = cif;
    }
}
